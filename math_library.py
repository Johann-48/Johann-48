def sum(num1, num2):
    num1 = float(num1)
    num2 = float(num2)
    r = num1 + num2
    return r
def sub(num1, num2):
    num1 = float(num1)
    num2 = float(num2)
    r = num1 - num2
    return r
def mul(num1, num2):
    num1 = float(num1)
    num2 = float(num2)
    r = num1 * num2
    return r
def div(num1, num2):
    num1 = float(num1)
    num2 = float(num2)
    r = num1 / num2
    return r
    
